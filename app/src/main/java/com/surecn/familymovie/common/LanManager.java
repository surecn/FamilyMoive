    public static void discover(Context context) {
        long [] res = getIpAddrMask();
        if (res == null) {
            return;
        }
        long ip = res[0];
        int length = (int) res[1];

        long min = ip & (0xffffffff << length);
        long max = ip | (0xffffffff >>> (32 - length));

        for (ip = min; ip <= max;ip++) {
            sendUDP(LongToIp(ip));
        }
    }

